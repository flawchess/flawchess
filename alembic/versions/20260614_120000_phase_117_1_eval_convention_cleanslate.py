    # Step 1: delete game_flaws derived from the wrong (pre-push) evals.
    op.execute(
        f"""
        DELETE FROM game_flaws gf
        USING games g
        WHERE gf.game_id = g.id
          AND {_ENGINE_FULL_DRAINED}
        """
    )

    # Step 2: NULL the mis-stored per-ply eval/best_move/pv on engine games.
    op.execute(
        f"""
        UPDATE game_positions gp
        SET eval_cp = NULL,
            eval_mate = NULL,
            best_move = NULL,
            pv = NULL
        FROM games g
        WHERE gp.game_id = g.id
          AND {_ENGINE_FULL_DRAINED}
        """
    )

