def upgrade() -> None:
    """NULL eval/best_move/pv for dense eval-only engine games (SEED-044 follow-up)."""
    op.execute(
        f"""
        WITH dense_engine_games AS (
            SELECT gp.game_id
            FROM game_positions gp
            JOIN games g ON g.id = gp.game_id
            WHERE g.lichess_evals_at IS NULL
              AND g.full_evals_completed_at IS NULL
            GROUP BY gp.game_id
            HAVING (
                SUM(CASE WHEN gp.eval_cp IS NOT NULL OR gp.eval_mate IS NOT NULL THEN 1 ELSE 0 END)::float
                / COUNT(*)
            ) >= {_COVERAGE_MIN}
        )
        UPDATE game_positions
        SET eval_cp = NULL, eval_mate = NULL, best_move = NULL, pv = NULL
        WHERE game_id IN (SELECT game_id FROM dense_engine_games)
        """
    )


